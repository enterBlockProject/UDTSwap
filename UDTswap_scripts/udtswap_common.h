uint128_t get_uint128_t(int from, uint8_t arr[]) {
  uint128_t ret=0;
  int i=0;
  for (i=0; i<UDT_AMOUNT_SIZE; i++) {
    ret += (uint128_t)arr[from+i] << (8*i);
  }
  return ret;
}